using namespace std;

int gcd(int a, int b) {
    while (b != 0) {
        int temp = b;
        b = a % b;
        a = temp;
    }
    return a;
}

int smallestGcdSum(vector<int>& arr) {
    sort(arr.begin(), arr.end());

    int current_gcd = arr[0];
    int total_sum = current_gcd;

    for (size_t i = 1; i < arr.size(); i++) {
        current_gcd = gcd(current_gcd, arr[i]);
        total_sum += current_gcd;
    }

    return total_sum;
}

void solve() {
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++) {
        cin >> arr[i]; 
    }
    
    cout << smallestGcdSum(arr) << endl;
}


